package com.example.chartDemo;

public class Student {
	  private String name;
	  private Integer computerMark;
	  private Integer mathematicsMark;
	  private Integer geographyMark;
	  private Integer historyMark;
	  private Integer litratureMark;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getComputerMark() {
		return computerMark;
	}
	public void setComputerMark(Integer computerMark) {
		this.computerMark = computerMark;
	}
	public Integer getMathematicsMark() {
		return mathematicsMark;
	}
	public void setMathematicsMark(Integer mathematicsMark) {
		this.mathematicsMark = mathematicsMark;
	}
	public Integer getGeographyMark() {
		return geographyMark;
	}
	public void setGeographyMark(Integer geographyMark) {
		this.geographyMark = geographyMark;
	}
	public Integer getHistoryMark() {
		return historyMark;
	}
	public void setHistoryMark(Integer historyMark) {
		this.historyMark = historyMark;
	}
	public Integer getLitratureMark() {
		return litratureMark;
	}
	public void setLitratureMark(Integer litratureMark) {
		this.litratureMark = litratureMark;
	}
	  
	  
}
